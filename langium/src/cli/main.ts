import type { Collection } from '../language/generated/ast.js';
import chalk from 'chalk';
import { Command } from 'commander';
import { CharacterSheetLanguageMetaData } from '../language/generated/module.js';
import { createCharacterSheetServices } from '../language/character-sheet-module.js';
import { extractAstNode } from './cli-util.js';
import { generateJavaScript } from './generator.js';
import { NodeFileSystem } from 'langium/node';

export const generateAction = async (fileName: string, opts: GenerateOptions): Promise<void> => {
    const services = createCharacterSheetServices(NodeFileSystem).CharacterSheet;
    const collection = await extractAstNode<Collection>(fileName, services);
    const generatedFilePath = generateJavaScript(collection, fileName, opts.destination);
    console.log(chalk.green(`JavaScript code generated successfully: ${generatedFilePath}`));
};

export type GenerateOptions = {
    destination?: string;
}

export default function(): void {
    const program = new Command();

    program
        // eslint-disable-next-line @typescript-eslint/no-var-requires
        .version(require('../../package.json').version);

    const fileExtensions = CharacterSheetLanguageMetaData.fileExtensions.join(', ');
    program
        .command('generate')
        .argument('<file>', `source file (possible file extensions: ${fileExtensions})`)
        .option('-d, --destination <dir>', 'destination directory of generating')
        .description('generates JavaScript code that prints "Hello, {name}!" for each greeting in a source file')
        .action(generateAction);

    program.parse(process.argv);
}
